export const classById = (id) => classes.find((c) => c.id === id);
/**
 * Returns classes scheduled for today.
 * Uses date-fns format() with new Date() to get the weekday name in English.
 * Falls back gracefully to a deterministic value when called on the server
 * during SSR — but because date-fns uses the runtime Date, behaviour is
 * predictable for a given render moment.
 */
export function getTodayClasses(reference = BUSINESS_TODAY) {
    const weekday = format(reference, "EEEE");
    return classes.filter((c) => c.days.includes(weekday));
}
